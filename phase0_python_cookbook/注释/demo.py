# 工具会识别（help() / IDE / 文档生成）


# ============================================================
# 2. 函数说明（Function Docstring） —— 使用三引号
# ============================================================
def example_function():
    """
    Demonstrates how to write a proper function docstring.

    This is NOT a comment.
    This is documentation for developers and tools.
    """
    pass


# ============================================================
# 3. 类说明（Class Docstring） —— 使用三引号
# ============================================================
class ExampleClass:
    """
    Example class to show class-level documentation.
    """

    def method(self):
        """
        Method-level documentation.
        """
        pass


# ============================================================
# 4. 工程级多行逻辑注释 —— 连续 #
# ============================================================
# 校验 API_KEY 是否被正确配置：
# 1. 防止 .env 未加载导致为 None
# 2. 防止开发者未替换模板中的占位符
# 3. 提前失败，避免后续鉴权错误难以定位
API_KEY = "your_api_key_here"
if not API_KEY or API_KEY == "your_api_key_here":
    raise ValueError("Invalid API_KEY configuration")


# ============================================================
# 5. 代码块逻辑分区注释（非常推荐，大型工程常用）
# ============================================================
# ================== DATA LOADING SECTION ==================
data = [1, 2, 3]

# ================== DATA PROCESSING SECTION ==================
processed = [x * 2 for x in data]


# ============================================================
# 6. 行尾注释（补充说明，不写长逻辑）
# ============================================================
result = sum(processed)  # 计算处理后的总和


