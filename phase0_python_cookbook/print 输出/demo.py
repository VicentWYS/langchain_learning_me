"""
print_examples.py

本文件演示 Python 中 print() 的常见与高级用法。
建议：边运行边观察输出效果。
"""

# =========================
# 1. 最基础的输出
# =========================
print("Hello Python")  # 输出：Hello Python


# =========================
# 2. 输出多个内容（默认用空格分隔）
# =========================
name = "Alice"
age = 20
print(name, age)  # 输出：Alice 20


# =========================
# 3. 修改分隔符 sep
# =========================
print("2026", "01", "23", sep="-")  # 输出：2026-01-23


# =========================
# 4. 修改结尾符 end（默认是换行 \n）
# =========================
print("Hello", end=" ")
print("World")  # 输出：Hello World（在同一行）


# =========================
# 5. 输出变量类型
# =========================
num = 10
print("num 的类型是：", type(num))  # 输出：num 的类型是： <class 'int'>


# =========================
# 6. 字符串格式化：f-string（最推荐）
# =========================
score = 95
print(f"{name} 的成绩是 {score}")  # 输出：Alice 的成绩是 95


# =========================
# 7. 占位符格式化 %（老项目常见）
# =========================
print("%s 的成绩是 %d" % (name, score))  # 输出：Alice 的成绩是 95


# =========================
# 8. format() 格式化（中期常见）
# =========================
print("{} 的成绩是 {}".format(name, score))  # 输出：Alice 的成绩是 95


# =========================
# 9. 保留小数位数
# =========================
pi = 3.1415926
print(f"π 保留两位小数：{pi:.2f}")  # 输出：π 保留两位小数：3.14


# =========================
# 10. 打印列表
# =========================
nums = [1, 2, 3]
print(nums)  # 输出：[1, 2, 3]


# =========================
# 11. 打印字典
# =========================
user = {"name": "Alice", "age": 20}
print(user)  # 输出：{'name': 'Alice', 'age': 20}


# =========================
# 12. 解包打印（非常常用）
# =========================
print(*nums)  # 输出：1 2 3


# =========================
# 13. 打印换行字符串
# =========================
print("第一行\n第二行")  
# 输出：
# 第一行
# 第二行


# =========================
# 14. 打印制表符
# =========================
print("姓名\t年龄")
print("Alice\t20")
# 输出对齐的表格效果


# =========================
# 15. 打印布尔值与表达式
# =========================
print(5 > 3)  # 输出：True


# =========================
# 16. 打印调试信息（项目中非常常见）
# =========================
x = 5
y = 8
print(f"[DEBUG] x={x}, y={y}, x+y={x+y}")
# 输出：[DEBUG] x=5, y=8, x+y=13


# =========================
# 17. 打印进度提示（配合 end 使用）
# =========================
for i in range(3):
    print(f"加载中{i}...", end=" ")
# 输出：加载中0... 加载中1... 加载中2...


# =========================
# 18. 输出到文件（高级但非常实用）
# =========================
with open("log.txt", "w", encoding="utf-8") as f:
    print("这行内容被写入文件", file=f)


# =========================
# 19. 打印对象的内存地址
# =========================
print(id(name))  # 输出：一个内存地址数字


# =========================
# 20. 打印 None
# =========================
result = None
print(result)  # 输出：None
